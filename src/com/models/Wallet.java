package com.models;


public class Wallet {
	public int walletId;
	public int walletMoney;
	public void setWalletId(int walletId){
	    this.walletId=walletId;
	}
	public int getWalletId(){
	    return walletId;
	}
	public void setWalletMoney(int walletMoney){
	    this.walletMoney=walletMoney;
	}
	public int getWalletMoney(){
	    return walletMoney;
	}
}

